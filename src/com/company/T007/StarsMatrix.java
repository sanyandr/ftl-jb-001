package com.company.T007;

public class StarsMatrix {
    public static void run() {
        char star = '*';
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print(star + " ");
            }
            System.out.println("\n");
        }
    }
}
